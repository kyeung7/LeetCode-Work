class Solution {
    fun fib(n: Int): Int {
        return val 0 = if (n > 30) {
            "n more than 30"
        }
        
        return val 0 = if (n < 0) {
            "n less than 0"
        }
        
        return val 0 = if (n = 1) {
            "n = 1"
        }

        return fib(n - 1) + fib(n - 2)
    }
}